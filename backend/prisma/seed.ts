import { PrismaClient } from "@prisma/client";
const prismaClient = new PrismaClient();

async function main() {
  for (let i = 0; i < cities.length; i++) {
    const city = cities[i];
    if (city.Province === "BC") {
      console.log(city.Name);
      await prismaClient.city.upsert({
        where: { name: city.Name },
        update: {},
        create: {
          name: city.Name,
          provinceId: 2,
        },
      });
    }
    if (city.Province === "ON") {
      console.log(city.Name);
      await prismaClient.city.upsert({
        where: { name: city.Name },
        update: {},
        create: {
          name: city.Name,
          provinceId: 1,
        },
      });
    }
  }

  for (let i = 0; i < areas.length; i++) {
    const area = areas[i];
    if (area.PreCode === "") {
      console.log(area.Name);
      const city = await prismaClient.city.findFirst({
        where: { name: area.Name },
      });
      if (city) {
        await prismaClient.area.upsert({
          where: { name: area.Name },
          update: {},
          create: {
            name: area.Name,
            code: area.Code,
            cityId: city.id,
          },
        });
      }
    }

    if (area.PreCode !== "") {
      console.log(area.Name);
      const precode = await prismaClient.area.findFirst({ code: area.PreCode });
      if (precode) {
        await prismaClient.subArea.upsert({
          where: { name: area.Name },
          update: {},
          create: {
            name: area.Name,
            areaId: precode.id,
            code: area.Code,
          },
        });
      }
    }
  }
}

main()
  .then(async () => {
    await prismaClient.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prismaClient.$disconnect();
    process.exit(1);
  });
